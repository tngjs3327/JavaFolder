package Weeks1_2.Exam1;

public class Box {
  private int width;
  private int length;
  private int height;

  void setWidth(int argWidth) {
    this.width = argWidth;
  }

  void setLength(int argLength) {
    this.length = argLength;
  }

  void setHeight(int argHeight) {
    this.height = argHeight;
  }

  int getWidth() {
    return this.width;
  }

  int getLength() {
    return this.length;
  }

  int getHeight() {
    return this.height;
  }

}
